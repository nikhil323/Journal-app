const mongoose = require('mongoose')

mongoose.connect(process.env.MONGODB_URL, { useNewUrlParser: true,
useUnifiedTopology: true,
useCreateIndex: true
})

const noteSchema = new mongoose.Schema({
    title: {
        type: String,
        trim: true,
        unique: true
    },
    body: {
        type: String
    }
})

const Post = mongoose.model("note", noteSchema)


